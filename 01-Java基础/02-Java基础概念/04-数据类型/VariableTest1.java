public class VariableTest1
{
    public static void main(String[] args)
    {
        //定义变量记录姓名
        String name = "黑马";
        //定义变量记录年龄
        int age = 18;
        //定义变量记录性别
        char gender = '男';
        //定义变量记录身高
        double height = 180;
        //定义变量记录是否单身
        boolean flage = true;
        //输出
        System.out.println(name);
        System.out.println(age);
        System.out.println(gender);
        System.out.println(height);
        System.out.println(flage);
    }
}