package com.javastudy.compareoperator;

public class CompareoperatorDemo {
    public static void main(String[] args) {
        // 1.==判断左右两边是否相等
        int a = 10;
        int b = 10;
        int c = 20;
        System.out.println(a == b);
        System.out.println(a == c);

        // 2.!=判断左右两边是否不相等
        System.out.println(a != b);
        System.out.println(a != c);
    }
}
