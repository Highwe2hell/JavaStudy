package com.javastudy.arithmeticoperator;

public class ArithmeticoperatorDemo2 {
    public static void main(String[] args) {
        // 除法
        System.out.println(10 / 2);
        System.out.println(10 / 3);
        System.out.println(10.0 / 3);

        // 取模,取余
        System.out.println(10 % 2);
        System.out.println(10 % 3);
    }
}
